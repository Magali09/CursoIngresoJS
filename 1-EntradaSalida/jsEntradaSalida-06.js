/*La función parseInt comprueba el primer argumento, una cadena, 
e intenta devolver un entero de la base especificada. 
Por ejemplo, una base de 10 indica una conversión a número decimal, 8 octal, 16 hexadecimal, y así sucesivamente. 
Para bases superiores a 10, las letras del alfabeto indican numerales mayores que 9. Por ejemplo, para números hexadecimales
 (base 16), se utiliza de la A hasta la F.

Si parseInt encuentra un carácter que no es un numeral de la base especificada, 
lo ignora a él y a todos los caracteres correctos siguientes, devolviendo el valor entero obtenido hasta ese punto. 
parseInt trunca los números en valores enteros. Se permiten espacios anteriores y posteriores. */


